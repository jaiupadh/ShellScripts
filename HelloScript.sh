# 'echo' command is used to display text. Over here in this scenario '>' operator is used to write the output into a text file named file.txt single line comment end. if the txt file already exists '>' operator replaces it with the new output.

echo "Welcome to Scripting ! ;)" >> file.txt

: '#! /bin/bash - specifies about the bash being used to run the script.
This is an example for multi-line comment'

